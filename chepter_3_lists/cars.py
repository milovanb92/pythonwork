# sort list alphabetically 
cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort()
print(cars)

# sort list alphabetically revert
cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort(reverse=True)
print(cars)

# sorted() function
cars = ['bmw', 'audi', 'toyota', 'subaru']

print("This is original list:")
print(cars)

print("\n This is sorted list:")
print(sorted(cars))

print("\n This is original list again:")
print(cars)

# reverse sort with list.reverse()
cars = ['bmw', 'audi', 'toyota', 'subaru']
print(f"\n {cars}")
cars.reverse()
print(cars)

# lenght of the list
cars = ['bmw', 'audi', 'toyota', 'subaru']
print(len(cars))