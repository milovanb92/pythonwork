motocycles = ['honda', 'yamaha', 'suzuki']
print(motocycles)

#motocycles[0] = 'ducati'
#print(motocycles)

# add item to beginning of the list using append
motocycles.append('ducati')
print(motocycles)

# add item to list on position with insert
motocycles = ['honda', 'yamaha', 'suzuki']
print(motocycles)
motocycles.insert(0, 'ducati')
print(motocycles)

# delete item from list using del
motocycles = ['honda', 'yamaha', 'suzuki']
print(motocycles)
del motocycles[0]
print(motocycles)

